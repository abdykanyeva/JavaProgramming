package day02_escapeSequences;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello Student. \nToday we have Java class"); //  \n sequence adds a new line
        System.out.println("------------------------------");
        System.out.println("\tJava Programming language");
        System.out.println("\t\t\tJava developer course batch 1");

        System.out.println("\\");  // To print 2 backslash you need to write 2 backslashes
        System.out.println("My favorite TV show is \"The Walking Dead \" ");


    }



}

/*
1. \n - new line
2. \t - tab
3. \\ - back slash
4. \" - double quote

 */