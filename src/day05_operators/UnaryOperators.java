package day05_operators;

public class UnaryOperators {
    public static void main(String[] args) {
        //pre Increment / Decrement

        int x = 10;

        System.out.println(++x);
        System.out.println(x);
        System.out.println(--x);

        System.out.println("-----------------------------------------------------------------");

        int a  = 50;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a);
        int b = 25;
        System.out.println(b--);
        System.out.println(b);

        System.out.println("-----------------------------------------");

        int n = 30;

        int m = n ++; 








    }
}
